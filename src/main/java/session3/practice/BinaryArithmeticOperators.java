package session3.practice;

public class BinaryArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        System.out.println("Arithmetic operations with int:");
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);

        double double1 = 10.5;
        double double2 = 4.5;

        System.out.println("Arithmetic operations with doubles:");
        System.out.println(double1 + double2);
        System.out.println(double1 - double2);
        System.out.println(double1 * double2);
        System.out.println(double1 / double2);
        System.out.println(double1 % double2);

        short myByte1 = 3;
        short myByte2 = 5;
        int resultFromBytes = myByte1 * myByte2;



    }
}
