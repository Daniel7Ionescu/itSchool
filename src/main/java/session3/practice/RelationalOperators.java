package session3.practice;

public class RelationalOperators {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 30;

        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 > num2);
        System.out.println(num1 <= num2);
    }
}
