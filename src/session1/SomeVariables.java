package session1;

public class SomeVariables {
    private int anInt = 7;
    private double aDouble = 5.3;
    private char aChar = 'A';
    private String aString = "Hi, I am a String!";

    public void showVariables(){
        System.out.println("Here's an integer: " + anInt);
        System.out.println("This is a double: " + aDouble);
        System.out.println("He's quite the character: " + aChar);
        System.out.println("Don't String me along: " + aString);
    }

}
